#include <iostream>

using namespace std;

void draw_cat() {
    cout << "  -------" << endl;
    cout << " | /\\_/\\ |" << endl;
    cout << " |( o o )|" << endl;
    cout << " | > ^ < |" << endl;
    cout << "  -------" << endl;
    cout << "Schrodinger" << endl;
}

int main(int argc, char *argv[]) {
    draw_cat();
    return 0;
}